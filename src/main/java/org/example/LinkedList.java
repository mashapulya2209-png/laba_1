package main.java.org.example;

/**
 * Интерфейс для управления последовательностью элементов.
 *
 * <p>Данная структура представляет собой цепочку элементов, где каждый элемент
 * хранит ссылку на следующий в последовательности. Позволяет эффективно
 * выполнять операции вставки и удаления в любой позиции.</p>
 *
 * @param <T> тип хранимых элементов
 */
public interface LinkedList<T> {

    /**
     * Добавляет новый элемент в конец цепочки.
     *
     * @param item элемент для добавления в последовательность
     * @throws IllegalArgumentException если переданный элемент равен null
     */
    void add(T item);

    /**
     * Извлекает элемент из указанной позиции цепочки.
     *
     * @param position позиция извлекаемого элемента (начинается с 0)
     * @return элемент, находящийся на заданной позиции
     * @throws IndexOutOfBoundsException если позиция некорректна
     *         (position < 0 || position >= getLength())
     */
    T get(int position);

    /**
     * Удаляет элемент из указанной позиции цепочки.
     * Все последующие элементы смещаются на одну позицию назад.
     *
     * @param position позиция удаляемого элемента
     * @return удаленный из цепочки элемент
     * @throws IndexOutOfBoundsException если позиция некорректна
     *         (position < 0 || position >= getLength())
     */
    T removeAt(int position);

    /**
     * Удаляет первый найденный элемент из цепочки.
     *
     * @param item элемент, который требуется удалить из цепочки
     * @return true, если элемент был найден и удален
     */
    boolean removeItem(T item);

    /**
     * Возвращает общее количество элементов в цепочке.
     *
     * @return число элементов в последовательности
     */
    int getLength();

    /**
     * Проверяет, является ли цепочка пустой.
     *
     * @return true, если в цепочке отсутствуют элементы
     */
    boolean isChainEmpty();

    /**
     * Очищает цепочку, удаляя все элементы.
     * После выполнения метода цепочка становится пустой.
     */
    void wipeAll();

    /**
     * Проверяет наличие элемента в цепочке.
     *
     * @param item элемент, наличие которого проверяется
     * @return true, если элемент присутствует в цепочке
     */
    boolean hasElement(T item);

    /**
     * Находит позицию первого вхождения элемента в цепочке.
     *
     * @param item искомый элемент
     * @return позицию первого найденного элемента или -1, если элемент не найден
     */
    int findPosition(T item);
}
